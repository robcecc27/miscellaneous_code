public class ShowShort
{
	public static void main(String[] args)
	{
		// Create a variable to hold a result.
		short MyVar = 2;
		
		// Display the initial value.
		System.out.println("The MyVar starting value equals: " + MyVar);
		
		// Add 5 to MyVar.
		MyVar += 5;

		// Display the result.
		System.out.println("Add 5 to MyVar equals: " + MyVar);
		
		// Subtract 4 from MyVar.
		MyVar -= 4;
		
		// Display the result.
		System.out.println("Subtract 4 from MyVar equals: " + MyVar);
		
		// Increment MyVar.
		MyVar++;

		// Display the result.
		System.out.println("Increment MyVar equals: " + MyVar);
		
		// Decrement MyVar.
		MyVar--;
		
		// Display the result.
		System.out.println("Decrement MyVar equals: " + MyVar);
		
		// Multiply MyVar by 6.
		MyVar *= 6;
		
		// Display the result.
		System.out.println("Multiply MyVar by 6 equals: " + MyVar);
		
		// Divide MyVar by 3.
		MyVar /= 3;
		
		// Display the result.
		System.out.println("Divide MyVar by 3 equals: " + MyVar);
		
		}
}